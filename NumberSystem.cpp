#include<bits/stdc++.h>
using namespace std;
//Number System
int main()
{
  int n1=15;//Decimal
  int n2=017;//Octal
  int n3=0x0f;//Hexa-Decimal
  int n4=0b00001111;//Binary-C++14
  cout<<"Number 1="<<n1<<endl;
  cout<<"Number 2="<<n2<<endl;
  cout<<"Number 3="<<n3<<endl;
  cout<<"Number 4="<<n4<<endl;
  return 0;  
}